CREATE TABLE IF NOT EXISTS hall
  (id INTEGER PRIMARY KEY NOT NULL, number_row
  SMALLINT NOT NULL, number_seat SMALLINT NOT NULL, price INTEGER NOT NULL, booked BOOLEAN NOT NULL);

CREATE TABLE IF NOT EXISTS accounts
  (id INTEGER PRIMARY KEY REFERENCES hall(id), user_name
  VARCHAR(150) NOT NULL, phone_number VARCHAR(15) NOT NULL);

